import React, { Component } from 'react';
import Scrollbar from 'react-material/Scrollbar';
import { SpringSystem, MathUtil } from 'rebound';

class SpringScrollbar extends Component {
  constructor(props, ...rest) {
    super(props, ...rest);
    this.handleSpringUpdate = this.handleSpringUpdate.bind(this);
    this.scrollbar = React.createRef();
  }

  componentDidMount() {
    this.springSystem = new SpringSystem();
    this.spring = this.springSystem.createSpring();
    this.spring.addListener({ onSpringUpdate: this.handleSpringUpdate });
  }

  componentWillUnmount() {
    this.springSystem.deregisterSpring(this.spring);
    this.springSystem.removeAllListeners();
    this.springSystem = undefined;
    this.spring.destroy();
    this.spring = undefined;
  }

  getScrollTop() {
    return this.scrollbar.current.getScrollTop();
  }

  getScrollHeight() {
    return this.scrollbar.current.getScrollHeight();
  }

  getHeight() {
    return this.scrollbar.current.getHeight();
  }

  scrollTop(top) {
    const scrollbar = this.scrollbar.current;
    const scrollTop = scrollbar.getScrollTop();
    const scrollHeight = scrollbar.getScrollHeight();
    const val = MathUtil.mapValueInRange(
      top,
      0,
      scrollHeight,
      scrollHeight * 0.2,
      scrollHeight * 0.8,
    );
    this.spring.setCurrentValue(scrollTop).setAtRest();
    this.spring.setEndValue(val);
  }

  handleSpringUpdate(spring) {
    const scrollbar = this.scrollbar.current;
    const val = spring.getCurrentValue();
    scrollbar.scrollTop(val);
  }

  render() {
    return <Scrollbar {...this.props} ref={this.scrollbar} />;
  }
}

