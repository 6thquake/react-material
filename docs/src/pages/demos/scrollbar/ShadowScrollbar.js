import React, { Component } from 'react';
import PropTypes from 'prop-types';
import Scrollbar from 'react-material/Scrollbar';

class ShadowScrollbar extends Component {
  constructor(props, ...rest) {
    super(props, ...rest);
    this.state = {
      scrollTop: 0,
      scrollHeight: 0,
      clientHeight: 0,
    };
    this.handleUpdate = this.handleUpdate.bind(this);
    this.shadowTop = React.createRef();
    this.shadowBottom = React.createRef();
  }

  handleUpdate(values) {
    const shadowTop = this.shadowTop.current;
    const shadowBottom = this.shadowBottom.current;
    const { scrollTop, scrollHeight, clientHeight } = values;
    const shadowTopOpacity = (1 / 20) * Math.min(scrollTop, 20);
    const bottomScrollTop = scrollHeight - clientHeight;
    const shadowBottomOpacity =
      (1 / 20) * (bottomScrollTop - Math.max(scrollTop, bottomScrollTop - 20));

    this.setState({
      shadowTopOpacity: shadowTopOpacity,
      shadowBottomOpacity: shadowBottomOpacity,
    });
  }

  render() {
    const { style, ...props } = this.props;
    const containerStyle = {
      ...style,
      position: 'relative',
    };

    const { shadowTopOpacity, shadowBottomOpacity } = this.state;

    const shadowTopStyle = {
      position: 'absolute',
      top: 0,
      left: 0,
      right: 0,
      height: 10,
      opacity: shadowTopOpacity,
      background: 'linear-gradient(to bottom, rgba(0, 0, 0, 0.2) 0%, rgba(0, 0, 0, 0) 100%)',
    };
    const shadowBottomStyle = {
      position: 'absolute',
      bottom: 0,
      left: 0,
      right: 0,
      height: 10,
      opacity: shadowBottomOpacity,
      background: 'linear-gradient(to top, rgba(0, 0, 0, 0.2) 0%, rgba(0, 0, 0, 0) 100%)',
    };
    return (
      <div style={containerStyle}>
        <Scrollbar onUpdate={this.handleUpdate} {...props} />
        <div ref={this.shadowTop} style={shadowTopStyle} />
        <div ref={this.shadowBottom} style={shadowBottomStyle} />
      </div>
    );
  }
}

